        //placeholder
        $('input[placeholder], textarea[placeholder]').placeholder();

        //contact form submit
        $(\"#contactform #submit\").click(function(){
                var hasError = false;

                var emailVal = $(\"#contactform #email\").val();
                if(emailVal == '') {
                    alert('You forgot to enter the email address.');
                    hasError = true;
                } else if(!isValidEmailAddress(emailVal)) {
                    alert('Enter a valid email address to send to.');
                    hasError = true;
                }

                var nameVal = $(\"#contactform #name\").val();
                if(nameVal == '') {
                    alert('You forgot to enter your name.');
                    hasError = true;
                }

                var messageVal = $(\"#contactform #message\").val();
                if(messageVal == '') {
                    alert('You forgot to enter the message.');
                    hasError = true;
                }

                if(hasError == false) {
                    $.post(\"sendemail.php\",{ ref:'contact', email: emailVal, name: nameVal, message: messageVal },function(data){
                        alert('Thank You. A member of our team will contact you shortly.');
                    });
                }
                return false;
            });


        //scroller
        $(\"#scroller\").simplyScroll({
            autoMode: 'loop',
            startOnLoad: true,
            pauseOnHover: false
        });

        //popup
        $(\".close-popup\").click(function(){
            $(\".pop-overlay\").hide();
            $(\".popup-wrap\").hide();
            $(\".popup-box\").hide();
        })


        //custom select
        $('p.sel select').customSelect();

        //Toggle available plan addons

        $.fn.toggleDisabled = function(){
            return this.each(function(){
                this.disabled = !this.disabled;
            });
        };

        $('.add-plan').click(function(e) {
            e.preventDefault();
            var obj = $($(this).attr('href'));
            obj.toggleDisabled();

            var isDisabled = Boolean(obj.attr('disabled'));

            if(isDisabled) {
                $(this).text('Add to plan');
            }
            else {
                $(this).text('Remove Add-on ');
            }
        });
